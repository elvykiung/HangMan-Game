let numberOfGuess = 13;
let winCount = 0;
let lossCount = 0;
let correctGuess = document.getElementById('correctGuess');
let lastCorrEl = document.getElementById('lastCorrectGuess');
let winCountEl = document.getElementById('winCount');
let guessLeftEl = document.getElementById('guessLeft');
let lossCountEl = document.getElementById('lossCount');
let wrongGuess = document.getElementById('user-Key');

//start with cover the guess letters
let coveredWord = '_'.repeat(guesswords.length);
//make the cover letters to be array (Create an Array from a String)
// myArr = [_,_,_,_,_]
let myArr = Array.from(coveredWord);
//update the correctGuess element with ______ join the myarray to string (Join the elements of an array into a string)
correctGuess.textContent = myArr.join('');

//reset fuction
function reset() {
  guesswords = totolGuess[Math.floor(Math.random() * totolGuess.length)];
  console.log(guesswords);
  coveredWord = '_'.repeat(guesswords.length);
  myArr = Array.from(coveredWord);
  correctGuess.textContent = myArr.join('');
  numberOfGuess = 13;
  guessLeftEl.textContent = numberOfGuess;
  wrongGuess.textContent = '';
}

//created key up function
document.onkeyup = function(event) {
  let foundIt = false;
  // compare the user key with guesswords everytime user press key
  for (var j = 0; j < guesswords.length; j++) {
    //if the key press letter equal to any of charter in guesswords is ture
    if (event.key === guesswords.charAt(j)) {
      foundIt = true;
      //replace myarr letter with guesswords
      myArr[j] = guesswords.charAt(j);
    }
  }

  if (foundIt) {
    //update the guessword to html element
    correctGuess.textContent = myArr.join('');
  } else if (!wrongGuess.textContent.match(event.key)) {
    numberOfGuess -= 1;
    guessLeftEl.textContent = numberOfGuess;
    wrongGuess.textContent += event.key;
  }

  // if guess words wrong, restart new guess word with new guess count
  if (numberOfGuess == 0) {
    lossCount += 1;
    reset();
    lossCountEl.textContent = lossCount;
  }
  // if guess words correct, restart new guess word with new guess count
  if (myArr.join('') === guesswords) {
    winCount += 1;
    winCountEl.textContent = winCount;
    lastCorrEl.textContent = guesswords;
    reset();
  }
};
